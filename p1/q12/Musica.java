package POO.p1.q12;

public class Musica {
    private String nome;
    private float duracao;


    public Musica(String nome, float duracao) {
        this.nome = nome;
        this.duracao = duracao;
    }

    public float getDuracao() {
        return duracao;
    }

    public String getNome() {
        return nome;
    }



}
