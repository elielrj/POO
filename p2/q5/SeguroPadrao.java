package POO.p2.q5;

public class SeguroPadrao {

	private String nome;
	private double valor;

	public SeguroPadrao(String nome, double valor) {
		this.nome = nome;
		this.valor = valor;
	}

	public String getNome() {
		return nome;
	}

	public void setValor(double valor) {
		this.valor = valor;
	}

	public double getValor() {
		return valor;
	}

}
